#Existe  7 operadores
#Suma,resta,mutiplo,division,Integer
#Exponente,Modulo o Residuo

opea=3
opeb=2

sum=opea+opeb
#print("Resultado de la suma",sum)

#F literal
print(f'Resutlado Suma real: {sum}')

#Resta
rest=opea-opeb
print(f'Resutlado Resta: {rest}')

#Mutiplo
multi=opea*opeb
print(f'Resutlado Multiplicacion: {multi}')

#Division
print(f'Resutlado Dividir: {opea/opeb}')

#Divi Operador doble Diagonal
print(f'Resutlado Dividir Entero: {opeb//opeb}')

#Residuo
mod=opea%opeb
print(f'Resultado Residuo Modulo {mod}')

expo=opea**opeb
print(f'Resultado Residuo Exponencial: {expo}')
