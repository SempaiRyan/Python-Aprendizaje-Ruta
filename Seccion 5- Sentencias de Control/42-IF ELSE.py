# Se define una variable booleana llamada 'condicion' y se le asigna el valor True
condicion = True

# Se evalúa si la variable 'condicion' es igual a True
if condicion == True:
    # Si la condición es verdadera, se imprime este mensaje
    print("Condicion Verdad")

# Si la condición no fue True, se evalúa si es False
elif condicion == False:
    # Si la condición es falsa, se imprime este mensaje
    print("Condicion Falso")

# Si la variable 'condicion' no es ni True ni False (otro valor), se ejecuta este bloque
else:
    print("Condicion no ELEGIBLE")
